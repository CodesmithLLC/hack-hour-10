/* Given a positive integer, return it as a string in Roman Numeral form.
 * Some basic conversions are given below:
 *
 *      1     ->    I
 *      4     ->    IV
 *      5     ->    V
 *      9     ->    IX
 *      10    ->    X
 *      40    ->    XL
 *      50    ->    L
 *      90    ->    XC
 *      100   ->    C
 *      400   ->    CD
 *      500   ->    D
 *      900   ->    CM
 *      1000  ->    M
 * 
 */

function romanNumeral(n) {
  const conversion = {
  1: 'I',
  4: 'IV',
  5: 'V',
  9: 'IX',
  10: 'X',
  40: 'XL',
  50: 'L',
  90: 'XC',
  100: 'C',
  400: 'CD',
  500: 'D',
  900: 'CM',
  1000: 'M'
  }

  let numbers = Object.keys(conversion);
  let result = '';
  let difference;
  let counter = 0;

  numbers.every(function(num) {
    if(n >= Number(num)) {
      difference = +num;
      return true;
    }
    return false;
  });
  
  while(n >= difference) {
    result += conversion[difference];
    n -= difference;
  }
  
  return result;
}
